print(f"New questions to add: {sorted(NEW_QUESTIONS_SET)}")

# Process all questions in order
for q_num in sorted(NEW_EXAM_QUESTIONS.keys()):
    q_text = NEW_EXAM_QUESTIONS[q_num]
    is_new = q_num in NEW_QUESTIONS_SET
    
    # Get answer text
    if is_new:
        answer_text = NEW_QA[q_num]['a']
        cn_text = NEW_QA[q_num]['cn']
    else:
        # Try to find in existing answers - use old numbering
        # In the 2025 PDF, the numbering was generally the same but some shifted
        old_num = q_num
        for old, new in OLD_NUM_MAP.items():
            if new == q_num:
                old_num = old
                break
        answer_text = existing.get(old_num, existing.get(q_num, ""))
        if not answer_text:
            answer_text = existing.get(q_num, "")
        cn_text = CN_TRANSLATIONS.get(q_num, "")
    
    # Add question header
    badge = " [NEW 2026]" if is_new else ""
    q_para = Paragraph(
        f"<b>{q_num}. {safe_text(q_text)}{badge}</b>",
        ParagraphStyle('Q', fontName='NotoSansBold', fontSize=10.5,
            spaceAfter=4, spaceBefore=10, textColor=colors.HexColor('#0f3460') if not is_new else colors.HexColor('#c0392b'),
            leading=14, leftIndent=0,
            borderColor=colors.HexColor('#0f3460') if not is_new else colors.HexColor('#c0392b'),
            borderWidth=0, borderPad=3)
    )
    story.append(q_para)
    
    # Add answer
    if answer_text:
        # Clean up the text - remove image references and form feeds
        clean_answer = answer_text.replace('\f', '\n')
        clean_answer = re.sub(r'!\[.*?\]', '[diagram]', clean_answer)
        # Take first ~600 chars to keep concise
        lines = [l.strip() for l in clean_answer.split('\n') if l.strip()]
        # Remove lines that are obviously TODO or repetitions of the question
        lines = [l for l in lines if not l.startswith('TODO') and len(l) > 2]
        # Join and truncate
        ans_text = '\n'.join(lines[:30])  # max 30 lines
        
        story.append(Paragraph(safe_text(ans_text), answer_style))
    
    # Add Chinese translation
    if cn_text:
        story.append(Paragraph("中文翻译 / Chinese Translation:", cn_label_style))
        story.append(Paragraph(safe_text(cn_text), cn_style))
    
    story.append(HRFlowable(width="100%", thickness=0.3, color=colors.lightgrey))

# Build
doc.build(story)
