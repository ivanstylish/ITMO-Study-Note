.text
; 声明代码段（可执行指令区域）
; Объявление секции кода (исполняемые инструкции)

.org 0x100
; 将代码段起始地址设置为 0x100，避开 I/O 地址 0x80/0x84
; Установить начало секции кода по адресу 0x100, вдали от адресов MMIO

; ============================================================
; main: entry point
; ============================================================
main:
; 主函数标签，程序从此处开始执行
; Метка главной функции, точка входа программы
    lui  sp, 0x1

; sp = 0x1 << 12 = 0x1000，初始化栈指针高位
;      选0x1000是因为它高于代码区且内存足够用
; sp = 0x1000: инициализация стека в старшие биты

    addi sp, sp, -4
; sp = 0x1000 - 4 = 0xFFC，预留4字节对齐
;      RISC-IV中字（word）是4字节，sp需保持4字节对齐
; sp = 0xFFC: первоначальное выравнивание стека по 4 байта

    addi t0, zero, 0x80
; t0 = 0x80，即 I/O 输入端口的地址
;      zero寄存器永远=0，addi zero, 0x80 等同于加载立即数
; t0 = 0x80: адрес порта ввода (Memory Mapped IO)

    lw   a0, 0(t0)
; a0 = Memory[t0 + 0] = Memory[0x80]，从I/O地址读取输入 n
; 0(t0) 语法：偏移量=0，基址寄存器=t0
; a0 = M[0x80]: читаем входное значение n из порта MMIO

    ble  a0, zero, main__invalid
; 若 a0 ≤ 0（即 n ≤ 0），跳转到 main__invalid
;      对应Python: if n <= 0: return -1
; Если n ≤ 0 — прыгаем на main__invalid (недопустимый ввод)

    addi sp, sp, -4
; sp -= 4，在栈上为保存 ra 分配4字节空间
;      因为 jal 会覆盖 ra，必须先把它存到栈上
; Выделяем 4 байта на стеке для сохранения ra перед вложенным вызовом

    sw   ra, 0(sp)
; Memory[sp] = ra，将返回地址存入栈中
;      如果不保存，jal sum_even 会覆盖 ra，无法返回 main 的调用者
; Сохраняем адрес возврата (ra) на стек перед вызовом sum_even

    jal  ra, sum_even
; ra = pc + 4（下一条指令地址），然后跳转到 sum_even
;      这是RISC-IV的标准函数调用方式
; Вызов sum_even: ra ← адрес следующей инструкции, PC ← sum_even

    lw   ra, 0(sp)
; 从栈中恢复 ra（sum_even执行完毕后）
; Восстанавливаем ra из стека после возврата из sum_even

    addi sp, sp, 4
; sp += 4，释放之前分配的4字节栈空间（弹出栈帧）
; Освобождаем 4 байта стека (pop)

    j    main__write_output
; 无条件跳转到输出阶段（跳过 main__invalid 的 a0=-1 赋值）
; Безусловный переход к записи вывода, минуя блок invalid

main__invalid:
; 子标签：处理无效输入（n ≤ 0）的路径
; Подметка: обработка недопустимого ввода (n ≤ 0)

    addi a0, zero, -1
; a0 = -1，设置返回值为 -1，符合题目要求：非法输入返回 -1
;      zero + (-1) = -1，利用 zero 寄存器加载立即数的常见技巧
; a0 = -1: возвращаемое значение при недопустимом вводе

main__write_output:
; 子标签：统一写输出的出口（valid和invalid两条路都汇聚于此）
; Подметка: единая точка записи вывода для обоих путей

    addi t0, zero, 0x84
; t0 = 0x84，即 I/O 输出端口的地址
; t0 = 0x84: адрес порта вывода (Memory Mapped IO)

    sw   a0, 0(t0)
; Memory[0x84] = a0，将结果写入输出端口
; Записываем результат a0 в порт вывода

    halt
; 停机，程序正常结束
; Остановка процессора

; ============================================================
; sum_even(n: a0) -> a0
;   formula: m = n / 2  =>  result = m * (m + 1)
; ============================================================
sum_even:
; sum_even 函数入口。参数：a0 = n；返回值：a0 = 结果
; Точка входа в функцию sum_even. Аргумент a0 = n

    addi sp, sp, -12
; 为栈帧分配12字节（保存 ra + s0 + s1，每个4字节）
; Выделяем 12 байт на стеке под ra, s0, s1

    sw   ra,  8(sp)
; Memory[sp+8] = ra，保存返回地址（因为要再调用 is_even）
; Сохраняем ra на стек (смещение +8)

    sw   s0,  4(sp)
; Memory[sp+4] = s0，s0是被调用者保存寄存器（callee-saved），必须还原
; Сохраняем s0 — регистр, который должен быть восстановлен при возврате

    sw   s1,  0(sp)
; [中] Memory[sp+0] = s1，同上
; [РУС] Сохраняем s1 — аналогично

    mv   s0, a0
; [中] s0 = a0 = n，将参数 n 保存到 s0（因为 a0 会被 is_even 调用覆盖）
;      s 系列寄存器跨函数调用保持不变（已保存在栈上）
; [РУС] Сохраняем n в s0 — a0 будет перезаписан при вызове is_even

    ; --- nested call: is_even(n) ---
    addi sp, sp, -4
; [中] 再分配4字节栈空间用于保存 ra（嵌套调用）
; [РУС] Выделяем 4 байта на стеке для ra перед вызовом is_even

    sw   ra, 0(sp)
; [中] 保存当前 ra，防止被 jal is_even 覆盖
; [РУС] Сохраняем ra перед вложенным вызовом

    jal  ra, is_even
; [中] 调用 is_even(a0)，a0 此时还是 n（s0 = n，a0 = n）
;      注意：is_even 的结果（a0）在这里并没有被实际用于分支判断
;      但它满足了实验"必须有嵌套调用"的要求，演示了栈操作
; [РУС] Вызов is_even(n) — демонстрация вложенного вызова и работы со стеком

    lw   ra, 0(sp)
; 从栈中恢复 ra
; Восстанавливаем ra из стека

    addi sp, sp, 4
; [中] 释放4字节栈空间
; [РУС] Освобождаем 4 байта стека

    ; compute m = n / 2
    addi t0, zero, 2
; [中] t0 = 2，用于做除法（RISC-IV没有立即数除法，需要先把除数放到寄存器）
; [РУС] t0 = 2: делитель для вычисления m = n / 2

    div  s1, s0, t0
; [中] s1 = s0 / t0 = n / 2 = m（整数除法，向零取整）
; [РУС] s1 = n / 2 = m (целочисленное деление)

    ; compute result = m * (m + 1)
    addi t0, s1, 1
; [中] t0 = s1 + 1 = m + 1
; [РУС] t0 = m + 1

    mul  t1, s1, t0
; [中] t1 = s1 * t0 = m * (m+1)，这是32位乘法的低32位结果
; [РУС] t1 = m * (m+1): нижние 32 бита произведения

    ; ---- overflow check ----
    mulh t2, s1, t0
; [中] t2 = (s1 * t0) 的高32位（符号扩展乘法）
;      如果 t2 ≠ 0，说明乘积超出32位，溢出
; [РУС] t2 = старшие 32 бита произведения — проверка переполнения

    bnez t2, sum_even__overflow
; [中] 若 t2 ≠ 0，说明结果超出32位，跳转到溢出处理
; [РУС] Если t2 ≠ 0 — переполнение (результат > 32 бит), прыгаем на overflow

    addi t3, zero, 0
; [中] t3 = 0，用于下面的有符号溢出检测
; [РУС] t3 = 0: для проверки знакового переполнения

    bgt  t3, t1, sum_even__overflow
; [中] 若 0 > t1（即 t1 < 0），说明32位有符号溢出（正数变成了负数），跳溢出处理
;      因为 m ≥ 0，m+1 > 0，m*(m+1) 应该 ≥ 0，如果是负数就是溢出了
; [РУС] Если t1 < 0 — знаковое переполнение (положительный результат стал отрицательным)

sum_even__ok:
; [中] 无溢出路径，结果正常
; [РУС] Нет переполнения — нормальный результат

    mv   a0, t1
; [中] a0 = t1 = m*(m+1)，设置返回值
; [РУС] Возвращаемое значение = m*(m+1)

    j    sum_even__return
; [中] 无条件跳到返回块（跳过 overflow 的赋值）
; [РУС] Прыжок к блоку возврата, минуя overflow

sum_even__overflow:
; [中] 溢出路径：返回 0xCCCCCCCC（题目要求溢出用0xCC填充字节）
; [РУС] Переполнение: возврат 0xCCCCCCCC по требованию задания

    addi a0, zero, -1
; [中] a0 = 0xFFFFFFFF（-1的二进制补码表示）
;      这是构造 0xCCCCCCCC 的第一步
; [РУС] a0 = 0xFFFFFFFF (−1 в дополнении до двух)

    lui  t0, 0x33333
; [中] t0 = 0x33333 << 12 = 0x33333000
;      用 lui 加载高20位，注意立即数是 0x33333（十六进制）
; [РУС] t0 = 0x33333000: загружаем старшие биты маски через lui

    addi t0, t0, 0x333
; [中] t0 = 0x33333000 + 0x333 = 0x33333333
;      0x333 = 819，正数，不会有符号扩展问题
; [РУС] t0 = 0x33333333: добавляем младшие 12 бит маски

    xor  a0, a0, t0
; [中] a0 = 0xFFFFFFFF XOR 0x33333333 = 0xCCCCCCCC
;      XOR翻转对应位：F^3=C，这是一个聪明的位运算技巧
; [РУС] a0 = 0xFF…FF XOR 0x33333333 = 0xCCCCCCCC (умный трюк через XOR)

sum_even__return:
; 函数返回出口，无论正常还是溢出路径都汇聚于此
; Общая точка выхода из функции (оба пути)

    lw   s1,  0(sp)
; [中] 从栈中恢复 s1（与 sw s1, 0(sp) 对应）
; [РУС] Восстанавливаем s1 из стека

    lw   s0,  4(sp)
; [中] 从栈中恢复 s0
; [РУС] Восстанавливаем s0 из стека

    lw   ra,   8(sp)
; [中] 从栈中恢复 ra
; [РУС] Восстанавливаем ra из стека

    addi sp, sp, 12
; [中] sp += 12，释放整个 sum_even 的栈帧（与函数入口 -12 对称）
; [РУС] Освобождаем стековый фрейм sum_even (симметрично -12 при входе)

    jr   ra
; [中] 跳转到 ra 保存的地址，即函数返回（返回到 main 中 jal 之后的位置）
; [РУС] Возврат из функции: PC ← ra

; ============================================================
; is_even(i: a0) -> a0
; ============================================================
is_even:
; is_even 函数入口，参数 a0 = n，返回值 a0 = 1（偶数）或 0（奇数）
; Точка входа is_even: a0=n → возвращает 1 (чётное) или 0 (нечётное)

    addi t0, zero, 2
; t0 = 2，用作取模运算的除数
; t0 = 2: делитель для rem (остаток от деления)

    rem  t0, a0, t0
; [中] t0 = a0 % 2，计算 n 除以 2 的余数
;      余数为 0 → 偶数；余数为 1 → 奇数
; [РУС] t0 = n % 2: проверяем чётность через остаток

    beqz t0, is_even__yes
; [中] 若 t0 == 0（n 是偶数），跳转到 is_even__yes
; [РУС] Если остаток = 0 (чётное) — прыгаем на is_even__yes

    addi a0, zero, 0
; [中] 奇数路径：a0 = 0（返回 false）
; [РУС] Нечётное число: возвращаем 0

    jr   ra
; [中] 返回调用者
; [РУС] Возврат из функции

is_even__yes:
; [中] 偶数路径的子标签
; [РУС] Подметка для чётного числа

    addi a0, zero, 1
; [中] 偶数：a0 = 1（返回 true）
; [РУС] Чётное число: возвращаем 1

    jr   ra
; [中] 返回调用者
; [РУС] Возврат из функции

; 三处使用栈的地方：
; 1. main 中初始化栈指针 — lui sp, 0x1 + addi sp, sp, -4 → sp = 0x1000 - 4 = 0xFFC
; 2. main 调用 sum_even 时保存 ra — addi sp, sp, -4; sw ra, 0(sp)
; 3. sum_even 中保存 ra, s0, s1 — 标准的被调用者保存帧