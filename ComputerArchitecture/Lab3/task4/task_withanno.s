.text                                    ; Объявление секции кода (исполняемые инструкции)

.org 0x100                               ; Установить начало секции кода по адресу 0x100, вдали от адресов MMIO

; ============================================================
; main: entry point
; ============================================================
main:                                    ; Метка главной функции, точка входа программы
    
    lui  sp, 0x1                         ; sp = 0x1 << 12 = 0x1000, инициализация стека; выбрано 0x1000, так как выше кода и достаточно памяти

    addi sp, sp, -4                      ; sp = 0xFFC, выравнивание стека по 4 байта; в RISC-IV слово 4 байта, sp должен быть выровнен

    addi t0, zero, 0x80                  ; t0 = 0x80, адрес порта ввода (MMIO); zero всегда 0, это загрузка константы

    lw   a0, 0(t0)                       ; a0 = M[0x80], чтение входного значения n; синтаксис 0(t0): смещение=0, база=t0

    ble  a0, zero, main__invalid         ; Если a0 ≤ 0 (n ≤ 0), переход на main__invalid; аналог Python: if n <= 0: return -1

    addi sp, sp, -4                      ; Выделение 4 байт на стеке для ra; jal перезапишет ra, нужно сохранить

    sw   ra, 0(sp)                       ; M[sp] = ra, сохранение адреса возврата; иначе jal sum_even перезапишет ra

    jal  ra, sum_even                    ; ra = pc + 4, переход к sum_even; стандартный вызов функции RISC-IV

    lw   ra, 0(sp)                       ; Восстановление ra из стека после возврата из sum_even

    addi sp, sp, 4                       ; Освобождение 4 байт стека (pop)

    j    main__write_output              ; Безусловный переход к записи вывода, минуя блок invalid

main__invalid:                           ; Подметка: обработка недопустимого ввода (n ≤ 0)

    addi a0, zero, -1                    ; a0 = -1, возвращаемое значение при ошибке; трюк загрузки константы через zero

main__write_output:                      ; Подметка: единая точка записи вывода

    addi t0, zero, 0x84                  ; t0 = 0x84, адрес порта вывода (MMIO)

    sw   a0, 0(t0)                       ; M[0x84] = a0, запись результата в порт вывода

    halt                                 ; Остановка процессора

; ============================================================
; sum_even(n: a0) -> a0
;   formula: m = n / 2  =>  result = m * (m + 1)
; ============================================================
sum_even:                                ; Точка входа sum_even. Аргумент a0 = n, возвращаемое значение a0

    addi sp, sp, -12                     ; Выделение 12 байт на стеке под ra, s0, s1 (по 4 байта)

    sw   ra,  8(sp)                      ; M[sp+8] = ra, сохранение адреса возврата (будет вложенный вызов)

    sw   s0,  4(sp)                      ; M[sp+4] = s0, сохранение callee-saved регистра s0

    sw   s1,  0(sp)                      ; M[sp+0] = s1, сохранение callee-saved регистра s1

    mv   s0, a0                          ; s0 = n, сохранение аргумента в s0; a0 будет перезаписан при вызове is_even; регистры s сохраняются между вызовами

    ; --- nested call: is_even(n) ---
    addi sp, sp, -4                      ; Выделение 4 байт на стеке для ra перед вызовом is_even

    sw   ra, 0(sp)                       ; Сохранение текущего ra перед вложенным вызовом

    jal  ra, is_even                     ; Вызов is_even(n); результат не используется, но демонстрирует вложенный вызов и работу со стеком

    lw   ra, 0(sp)                       ; Восстановление ra из стека

    addi sp, sp, 4                       ; Освобождение 4 байт стека

    ; compute m = n / 2
    addi t0, zero, 2                     ; t0 = 2, делитель; RISC-IV не имеет деления на константу напрямую

    div  s1, s0, t0                      ; s1 = n / 2 = m (целочисленное деление)

    ; compute result = m * (m + 1)
    addi t0, s1, 1                       ; t0 = m + 1

    mul  t1, s1, t0                      ; t1 = m * (m+1), младшие 32 бита произведения

    ; ---- overflow check ----
    mulh t2, s1, t0                      ; t2 = старшие 32 бита произведения; если t2 ≠ 0, произведение превышает 32 бита

    bnez t2, sum_even__overflow          ; Если t2 ≠ 0, переполнение, переход на overflow

    addi t3, zero, 0                     ; t3 = 0, для проверки знакового переполнения

    bgt  t3, t1, sum_even__overflow      ; Если t1 < 0, знаковое переполнение (положительное стало отрицательным); т.к. m≥0 и m+1>0, результат должен быть ≥0

sum_even__ok:                            ; Нет переполнения — нормальный результат

    mv   a0, t1                          ; a0 = m*(m+1), установка возвращаемого значения

    j    sum_even__return                ; Переход к блоку возврата, минуя overflow

sum_even__overflow:                      ; Переполнение: возврат 0xCCCCCCCC

    addi a0, zero, -1                    ; a0 = 0xFFFFFFFF (−1 в дополнении до двух); первый шаг конструирования 0xCCCCCCCC — маска всех единиц

    lui  t0, 0x33333                     ; t0 = 0x33333000, загрузка старших битов маски через lui

    addi t0, t0, 0x333                   ; t0 = 0x33333333, добавление младших 12 бит; 0x333=819, укладывается в 12-битный знаковый формат (-2048~2047)

    xor  a0, a0, t0                      ; a0 = 0xFF…FF XOR 0x33333333 = 0xCCCCCCCC; XOR инвертирует биты: F^3=C

sum_even__return:                        ; Общая точка выхода из функции

    lw   s1,  0(sp)                      ; Восстановление s1 из стека

    lw   s0,  4(sp)                      ; Восстановление s0 из стека

    lw   ra,   8(sp)                     ; Восстановление ra из стека

    addi sp, sp, 12                      ; Освобождение стекового фрейма sum_even (симметрично -12 при входе)

    jr   ra                              ; Возврат из функции: PC ← ra

; ============================================================
; is_even(i: a0) -> a0
; ============================================================
is_even:                                 ; Точка входа is_even: a0=n → возвращает 1 (чётное) или 0 (нечётное)

    addi t0, zero, 2                     ; t0 = 2, делитель для остатка

    rem  t0, a0, t0                      ; t0 = n % 2, остаток от деления; 0 → чётное, 1 → нечётное

    beqz t0, is_even__yes                ; Если остаток 0 (чётное), переход на is_even__yes

    addi a0, zero, 0                     ; Нечётное: a0 = 0 (возвращаем false)

    jr   ra                              ; Возврат из функции

is_even__yes:                            ; Подметка для чётного числа

    addi a0, zero, 1                     ; Чётное: a0 = 1 (возвращаем true)

    jr   ra                              ; Возврат из функции

; ============================================================
; Сводка использования стека:
; 1. Инициализация в main: lui sp, 0x1 + addi sp, sp, -4 → sp = 0xFFC
; 2. Сохранение ra в main перед вызовом sum_even: addi sp, sp, -4; sw ra, 0(sp)
; 3. Сохранение ra, s0, s1 в sum_even: стандартный фрейм callee-saved, 12 байт
; 4. Сохранение ra в sum_even перед вызовом is_even: addi sp, sp, -4; sw ra, 0(sp)
; ============================================================

; ============================================================
; Почему не собрать 0xCCCCCCCC напрямую?
; Если напрямую использовать `lui t0, 0xCCCCC`, старшие 20 бит будут работать
; корректно; однако при выполнении `addi t0, t0, 0xCCC` десятичное значение
; 0xCCC равно 3276, что превышает максимальное значение 2047 для 12-битного
; знакового числа. Процессор рассмотрит его как отрицательное число и выполнит
; расширение знака, что приведет к ошибочному результату сложения.
; Использование преобразования через 0x33333333 — это изящный программный трюк,
; позволяющий идеально избежать ловушки расширения знака.
; ============================================================