; Алгоритм реализован без циклов, с использованием математической формулы. Сначала из порта ввода читается n. 
Если n ≤ 0, программа возвращает -1. Для вычисления суммы я использую формулу m * (m + 1), где m = n / 2 (целочисленное деление). 
Для демонстрации вложенных вызовов и работы со стеком вызывается функция is_even. 
После умножения обязательно проверяется переполнение 32 бит: если старшие биты (mulh) не равны нулю или результат оказался отрицательным 
(знаковое переполнение), возвращается 0xCCCCCCCC (собранный через XOR из-за ограничений 12-битных констант), иначе — корректный результат, 
который записывается в порт вывода.  
该算法不使用循环，而是通过数学公式实现。首先，从输入端口读取 n。如果 n ≤ 0，程序返回 -1。为了计算和，我使用公式 m * (m + 1)，其中 m = n / 2（整数除法）。
为了演示嵌套调用和堆栈操作，调用了 is_even 函数。乘法运算后，必须进行 32 位溢出检查：如果最高位（mulh）非零或结果为负数（有符号溢出），则返回 0xCCCCCCCC
（由于 12 位常量的限制，通过异或运算汇编）。否则，将正确结果写入输出端口。  


x0zero 永远是0，硬连线，写入无效  
x1 ra Return Address（返回地址）  
x2 sp Stack Pointer（栈指针）  
x10–x17 a0–a7 函数参数 / 返回值  
x5–x7, x28–x31 t0–t6 临时寄存器（调用者保存）  
x8–x9, x18–x27 s0–s11 保存寄存器（被调用者保存）  


.text                                    ; 声明代码段（可执行指令区域）

.org 0x100                               ; 将代码段起始地址设置为 0x100，避开 I/O 地址 0x80/0x84

; ============================================================
; main: entry point
; ============================================================
main:                                    ; 主函数标签，程序从此处开始执行
    
    lui  sp, 0x1                         ; sp = 0x1 << 12 = 0x1000，初始化栈指针高位；选0x1000是因为它高于代码区且内存足够用

    addi sp, sp, -4                      ; sp = 0x1000 - 4 = 0xFFC，预留4字节对齐；RISC-IV中字是4字节，sp需保持4字节对齐

    addi t0, zero, 0x80                  ; t0 = 0x80，即 I/O 输入端口的地址；zero寄存器永远=0，等同于加载立即数

    lw   a0, 0(t0)                       ; a0 = Memory[t0 + 0] = Memory[0x80]，从I/O地址读取输入 n；0(t0) 语法：偏移量=0，基址寄存器=t0

    ble  a0, zero, main__invalid         ; 若 a0 ≤ 0（即 n ≤ 0），跳转到 main__invalid；对应Python: if n <= 0: return -1

    addi sp, sp, -4                      ; sp -= 4，在栈上为保存 ra 分配4字节空间；因为 jal 会覆盖 ra，必须先把它存到栈上

    sw   ra, 0(sp)                       ; Memory[sp] = ra，将返回地址存入栈中；如果不保存，jal sum_even 会覆盖 ra，无法返回 main 的调用者

    jal  ra, sum_even                    ; ra = pc + 4（下一条指令地址），然后跳转到 sum_even；这是RISC-IV的标准函数调用方式

    lw   ra, 0(sp)                       ; 从栈中恢复 ra（sum_even执行完毕后）

    addi sp, sp, 4                       ; sp += 4，释放之前分配的4字节栈空间（弹出栈帧）

    j    main__write_output              ; 无条件跳转到输出阶段（跳过 main__invalid 的 a0=-1 赋值）

main__invalid:                           ; 子标签：处理无效输入（n ≤ 0）的路径

    addi a0, zero, -1                    ; a0 = -1，设置返回值为 -1，符合题目要求：非法输入返回 -1；利用 zero 寄存器加载立即数的常见技巧

main__write_output:                      ; 子标签：统一写输出的出口（valid和invalid两条路都汇聚于此）

    addi t0, zero, 0x84                  ; t0 = 0x84，即 I/O 输出端口的地址

    sw   a0, 0(t0)                       ; Memory[0x84] = a0，将结果写入输出端口

    halt                                 ; 停机，程序正常结束

; ============================================================
; sum_even(n: a0) -> a0
;   formula: m = n / 2  =>  result = m * (m + 1)
; ============================================================
sum_even:                                ; sum_even 函数入口。参数：a0 = n；返回值：a0 = 结果

    addi sp, sp, -12                     ; 为栈帧分配12字节（保存 ra + s0 + s1，每个4字节）

    sw   ra,  8(sp)                      ; Memory[sp+8] = ra，保存返回地址（因为要再调用 is_even）

    sw   s0,  4(sp)                      ; Memory[sp+4] = s0，s0是被调用者保存寄存器，必须还原

    sw   s1,  0(sp)                      ; Memory[sp+0] = s1，同上

    mv   s0, a0                          ; s0 = a0 = n，将参数 n 保存到 s0（因为 a0 会被 is_even 调用覆盖）；s 系列寄存器跨函数调用保持不变（已保存在栈上）

    ; --- nested call: is_even(n) ---
    addi sp, sp, -4                      ; 再分配4字节栈空间用于保存 ra（嵌套调用）

    sw   ra, 0(sp)                       ; 保存当前 ra，防止被 jal is_even 覆盖

    jal  ra, is_even                     ; 调用 is_even(a0)，a0 此时还是 n；注意：is_even 的结果在这里并没有被实际用于分支判断，但满足了实验嵌套调用要求，演示了栈操作

    lw   ra, 0(sp)                       ; 从栈中恢复 ra

    addi sp, sp, 4                       ; 释放4字节栈空间

    ; compute m = n / 2
    addi t0, zero, 2                     ; t0 = 2，用于做除法（RISC-IV没有立即数除法，需要先把除数放到寄存器）

    div  s1, s0, t0                      ; s1 = s0 / t0 = n / 2 = m（整数除法，向零取整）

    ; compute result = m * (m + 1)
    addi t0, s1, 1                       ; t0 = s1 + 1 = m + 1

    mul  t1, s1, t0                      ; t1 = s1 * t0 = m * (m+1)，这是32位乘法的低32位结果

    ; ---- overflow check 溢出检查----
    mulh t2, s1, t0                      ; t2 = (s1 * t0) 的高32位（符号扩展乘法）；如果 t2 ≠ 0，说明乘积超出32位，溢出

    bnez t2, sum_even__overflow          ; 若 t2 ≠ 0，说明结果超出32位，跳转到溢出处理

    addi t3, zero, 0                     ; t3 = 0，用于下面的有符号溢出检测

    bgt  t3, t1, sum_even__overflow      ; 若 0 > t1（即 t1 < 0），说明32位有符号溢出（正数变成了负数）；因为 m ≥ 0，m+1 > 0，m*(m+1) 应该 ≥ 0，如果是负数就是溢出了

sum_even__ok:                            ; 无溢出路径，结果正常

    mv   a0, t1                          ; a0 = t1 = m*(m+1)，设置返回值

    j    sum_even__return                ; 无条件跳到返回块（跳过 overflow 的赋值）

sum_even__overflow:                      ; 溢出路径：返回 0xCCCCCCCC（题目要求溢出用0xCC填充字节）

    addi a0, zero, -1                    ; a0 = 0xFFFFFFFF（-1的二进制补码表示）；这是构造 0xCCCCCCCC 的第一步，获取全1掩码

    lui  t0, 0x33333                     ; t0 = 0x33333 << 12 = 0x33333000；用 lui 加载高20位，注意立即数是 0x33333（十六进制）

    addi t0, t0, 0x333                   ; t0 = 0x33333000 + 0x333 = 0x33333333；0x333 = 819，正数，不会有符号扩展问题，在12位有符号立即数范围内（-2048~2047）

    xor  a0, a0, t0                      ; a0 = 0xFFFFFFFF XOR 0x33333333 = 0xCCCCCCCC；XOR翻转对应位：F^3=C，这是一个聪明的位运算技巧

sum_even__return:                        ; 函数返回出口，无论正常还是溢出路径都汇聚于此

    lw   s1,  0(sp)                      ; 从栈中恢复 s1（与 sw s1, 0(sp) 对应）

    lw   s0,  4(sp)                      ; 从栈中恢复 s0

    lw   ra,   8(sp)                     ; 从栈中恢复 ra

    addi sp, sp, 12                      ; sp += 12，释放整个 sum_even 的栈帧（与函数入口 -12 对称）

    jr   ra                              ; 跳转到 ra 保存的地址，即函数返回（返回到 main 中 jal 之后的位置）

; ============================================================
; is_even(i: a0) -> a0
; ============================================================
is_even:                                 ; is_even 函数入口，参数 a0 = n，返回值 a0 = 1（偶数）或 0（奇数）

    addi t0, zero, 2                     ; t0 = 2，用作取模运算的除数

    rem  t0, a0, t0                      ; t0 = a0 % 2，计算 n 除以 2 的余数；余数为 0 → 偶数；余数为 1 → 奇数

    beqz t0, is_even__yes                ; 若 t0 == 0（n 是偶数），跳转到 is_even__yes

    addi a0, zero, 0                     ; 奇数路径：a0 = 0（返回 false）

    jr   ra                              ; 返回调用者

is_even__yes:                            ; 偶数路径的子标签

    addi a0, zero, 1                     ; 偶数：a0 = 1（返回 true）

    jr   ra                              ; 返回调用者

; ============================================================
; 栈使用总结：
; 1. main 中初始化栈指针：lui sp, 0x1 + addi sp, sp, -4 → sp = 0xFFC
; 2. main 调用 sum_even 时保存 ra：addi sp, sp, -4; sw ra, 0(sp)
; 3. sum_even 中保存 ra, s0, s1：标准被调用者保存帧，共 12 字节
; 4. sum_even 调用 is_even 时保存 ra：addi sp, sp, -4; sw ra, 0(sp)
; ============================================================

; ============================================================
; 为什么不直接拼 0xCCCCCCCC？
; 如果直接用 lui t0, 0xCCCCC，高 20 位没问题；但在执行 addi t0, t0, 
; 0xCCC 时，0xCCC 的十进制是 3276，超出了 12 位有符号数的最大值 2047，
; CPU 会将其当作负数进行符号扩展，从而导致加法结果错误。
; 利用 0x33333333 转换则是完美避开了符号扩展陷阱的编程技巧
; ============================================================