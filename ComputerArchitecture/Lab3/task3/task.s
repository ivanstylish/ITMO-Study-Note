; Выходной буфер функционирует аналогично «транзакции» в базе данных.
; Он позволяет программе временно хранить полный результат для окончательной проверки длины (cmp.l 0x40, D3).
; Только когда подтвердится, что результат находится в пределах 64 байт, данные «фиксируются» в фактическом порту ввода-вывода;
; если они превышают лимит, буферизованные данные отбрасываются, и выводится код ошибки.
; 它允许程序先把完整的结果暂存起来，进行最终的长度验证（cmp.l 0x40, D3）。
; 只有当确认结果未超过 64 字节时，才统一将数据“提交”到真实的 IO 端口；
; 如果超限，则丢弃缓冲区数据，输出错误码。

.text
.org 0x0200

; 设置各个指针和计数器。将 input_buf 地址加载到 A0，IO端口地址加载到 A2、A3，并将输入长度计数器 D4 清零。
; Начальная настройка. Загружает адрес input_buf в A0, адреса портов ввода/вывода в A2 и A3, 
; обнуляет счетчик длины ввода D4.

; 一：程序入口
_start:
    movea.l input_buf, A0  ; 将 input_buf 的地址加载到 A0
    movea.l 0x80, A2       ; A2 指向 IO 输入端口 (0x80)
    movea.l 0x84, A3       ; A3 指向 IO 输出端口 (0x84)
    move.l 0, D4           ; 初始化 D4 为 0，用于记录读取了多少字节

; 二：读取输入阶段
; 读取循环，不断从 0x80 端口读取字符。如果读了64个字符还没遇到换行符（D4 >= 0x40），则跳转到溢出处理；如果是换行符 \n，跳转到 read_done；否则将字符存入输入缓冲区，指针 A0 后移，D4 加1，继续循环。
; Цикл чтения. Считывает символы из порта 0x80. Если считано 64 символа без \n, переходит к обработке переполнения. Если \n — переходит к read_done. 
; Иначе сохраняет символ в буфер, сдвигает указатель A0 и увеличивает D4.

read_loop:
    cmp.l 0x40, D4         ; 比较 D4 和 64 (0x40)
    bge overflow           ; 如果 D4 >= 64，跳转到 overflow 处理
    move.b (A2), D0        ; 从 IO 端口 (A2) 读取 1 字节到 D0 (地址间接寻址)
    cmp.b 0x0A, D0         ; 检查是否为换行符 '\n' (ASCII 0x0A)
    beq read_done          ; 如果是换行符，跳转到读取完成
    move.b D0, (A0)+       ; 将字符存入 input_buf，A0 自动后移 1 字节 (后置增量寻址)
    add.l 1, D4            ; D4++ (记录读取的总数)
    jmp read_loop          ; 继续循环

; 读取完成，重置 A0 指向 input_buf 开头。检查 D4 是否为0（空输入）。如果为空，直接停机；否则跳转到 compress_init 开始压缩。
; Чтение завершено. Сбрасывает A0 на начало input_buf. 
; Проверяет, пуст ли ввод (D4=0). Если пуст, останавливает программу; иначе переходит к инициализации сжатия.

read_done:
    movea.l input_buf, A0  ; A0 重新指向缓冲区开头，准备处理
    cmp.l 0, D4            ; 检查输入是否为空
    bne compress_init      ; 如果不为空，开始压缩
    halt                   ; 为空则直接停机

; 三：压缩处理阶段
; 初始化压缩所需的变量。A1 指向 output_buf，D3（输出长度）清零。从输入缓冲区读取第一个字符作为当前统计字符存入 D2，设置初始计数 D1=1，已读字符数 D4 减1。
; Инициализация сжатия. Устанавливает A1 на output_buf, обнуляет D3 (длина вывода). 
; Считывает первый символ в D2 как текущий символ серии, устанавливает счетчик D1=1, уменьшает D4.

compress_init:
    movea.l output_buf, A1 ; A1 指向输出暂存区 Возвращает указатель A0 на начало считанных данных.
    move.l 0, D3           ; D3 为输出长度计数 Обнуляет D3 (счетчик длины выходных данных).
    move.b (A0)+, D2       ; 取第一个字符存入 D2，A0 后移 Считывает самый первый символ в D2 для начала отсчета серии.
    move.l 1, D1           ; 初始化连续计数器 D1 = 1 Устанавливает начальный счетчик повторений текущего символа в 1.
    sub.l 1, D4            ; 总字符数减 1 Уменьшает количество оставшихся для обработки символов.

; 核心逻辑。如果输入已读完（D4=0）或连续计数达到9（D1=9），跳转到 flush_run 输出当前统计。否则读取下一个字符到D0，D4 减1。比较新字符是否与当前字符（D2）相同：相同则 D1 加1，继续循环；不同则跳转到 flush_and_new。
; Основной цикл сравнения. Если ввод закончился (D4=0) или счетчик достиг 9 (D1=9), переходит к записи серии. 
; Иначе считывает следующий символ, уменьшает D4. Если символ совпадает с D2, увеличивает D1; если нет — переходит к flush_and_new.

char_loop:
    cmp.l 0, D4            ; 检查输入是否处理完 Проверьте, обработаны ли входные данные.
    beq flush_run          ; 完结，刷入最后一组统计 Если символы закончились, записывает последнюю серию в выход.
    cmp.l 9, D1            ; 检查计数是否达到 9 Проверяет, не достигла ли текущая серия лимита в 9 символов.
    beq flush_run          ; 达到 9，强制刷入 Если достигла 9, записывает серию и начинает новую (требование алгоритма).
    move.b (A0)+, D0       ; 取下一个字符到 D0
    sub.l 1, D4            ; 待处理数减 1 Уменьшает счетчик оставшихся байтов.
    cmp.b D2, D0           ; 比较新字符 D0 与当前统计字符 D2 Сравнивает новый символ (D0) с текущим символом серии (D2).
    bne flush_and_new      ; 如果字符不同，跳转处理 Если символы разные, записывает старую серию и начинает новую.
    add.l 1, D1            ; 字符相同，D1++ Если символы одинаковые, увеличивает счетчик серии на 1.
    jmp char_loop          ; Переходит к следующей итерации сравнения.

; 刷入当前统计，将当前的统计结果（D1 和 D2）写入输出缓冲区。输出长度 D3 加2。如果 D3 超过64，触发溢出。将 D1 加上 0x30 变成 ASCII 数字，存入缓冲区，再将字符 D2 存入。
; 如果输入没读完，读取下一个字符作为新的 D2，重置 D1=1，继续 char_loop；如果读完了，跳转到 compress_done。
; Эта метка для Обновления текущих статистических данных, запись текущей серии в вывод. Увеличивает D3 на 2. Если D3 > 64, вызывает переполнение. 
; Преобразует D1 в ASCII-цифру (+0x30) и записывает, затем записывает символ D2. 
; Если ввод не закончился, берет следующий символ как новый D2, сбрасывает D1=1 и возвращается к циклу; иначе переходит к завершению.

flush_run:
    add.l 2, D3            ; 每次写入产生 2 字节（数字+字符）
    cmp.l 0x40, D3         ; 检查输出是否超过 64 字节
    bge overflow           ; Если результат слишком длинный, переходит к ошибке.
    move.l D1, D0          ; 将计数值 D1 移入 D0 Копирует число повторений в D0 для обработки.
    add.b 0x30, D0         ; 将 1-9 转换为 ASCII '1'-'9' (0x30 为 '0') Преобразовать числа от 1 до 9 в ASCII-код '1'-'9' (0x30 — это '0').
    move.b D0, (A1)+       ; 写入数字到输出缓冲区 Записать числа в выходной буфер
    move.b D2, (A1)+       ; 写入字符到输出缓冲区 Записать символ в выходной буфер
    cmp.l 0, D4            ; 如果输入已空
    beq compress_done      ; 压缩完成
    move.b (A0)+, D2       ; 否则取新字符作为下一轮统计对象
    sub.l 1, D4            ; Уменьшает счетчик оставшихся байтов.
    move.l 1, D1           ; 重置计数为 1 Сбрасывает счетчик повторений в 1 для новой серии.
    jmp char_loop          ; Переходит к следующей итерации сравнения.

; 刷入统计并处理新字符，当遇到与当前字符不同的新字符时触发。先把新字符暂存到 D5。
; 接下来的写入逻辑与 flush_run 相同（写数字和旧字符）。写入完成后，把暂存在 D5 的新字符移入 D2 作为新的统计字符，重置 D1=1，跳回 char_loop。
; Эта метка для Обновления статистики и обработки новых символов, срабатывает при несовпадении символов. Сохраняет новый символ в D5. 
; Записывает серию старого символа (как в flush_run). 
; После записи переносит новый символ из D5 в D2, сбрасывает счетчик D1=1 и возвращается к char_loop.

flush_and_new:
    move.b D0, D5           ; (В flush_and_new) Временно сохраняет новый символ, чтобы не потерять его при записи серии.
    add.l 2, D3             ; Увеличивает размер выходных данных на 2 байта (цифра + сам символ).
    cmp.l 0x40, D3          ; Проверяет, не превысил ли сжатый результат 64 байта.
    bge overflow            ; Если результат слишком длинный, переходит к ошибке.
    move.l D1, D0           ; Копирует число повторений в D0 для обработки.
    add.b 0x30, D0          ; Преобразует число (1-9) в ASCII-код цифры ('1'-'9') путем добавления 0x30.
    move.b D0, (A1)+        ; Записывает ASCII-цифру в выходной буфер.
    move.b D2, (A1)+        ; Записывает сам символ в выходной буфер.
    move.b D5, D2           ; 将新字符 D5 存入 D2 成为新的统计目标 Устанавливает сохраненный новый символ как текущий для следующей серии.
    move.l 1, D1            ; 重置计数 Сбрасывает счетчик повторений в 1 для новой серии.
    jmp char_loop           ; Переходит к следующей итерации сравнения.

; 四：输出和结束阶段
; 压缩完成，在输出缓冲区末尾写入 \0（空字符结束符）。重置 A1 指向 output_buf 的开头，准备开始输出。
; Сжатие завершено. Записывает нуль-терминатор \0 в конец буфера вывода. 
; Сбрасывает A1 на начало output_buf для начала вывода.

compress_done:
    move.b 0, (A1)         ; 写入字符串结束符 \0
    movea.l output_buf, A1 ; A1 回到输出缓冲区开头

; 输出循环，逐个字节从输出缓冲区读取数据。如果是 \0，说明输出完毕，跳转到 done；否则将字节写入输出端口 0x84，继续循环。
; Цикл вывода. Считывает данные из буфера вывода байтом за байтом. 
; Если \0, переходит к завершению; иначе записывает байт в порт вывода 0x84 и продолжается.

output_loop:
    move.b (A1)+, D0       ; 从缓冲区取出一字节
    cmp.b 0, D0            ; 是否为结束符(与当前字符比较)
    beq done
    move.b D0, (A3)        ; 将字符写入 IO 输出端口 (0x84)
    jmp output_loop

; 溢出错误处理，当输入超过64字符，或压缩后的结果超过64字节时触发。向输出端口写入错误码 0xCCCCCCCC（对应十进制 -858993460），然后停机。
; Обработка переполнения. Срабатывает, если ввод превышает 64 символа или сжатый результат превышает 64 байта. 
; Записывает код ошибки 0xCCCCCCCC в порт вывода и останавливает программу.

overflow:
    move.l 0xCCCCCCCC, D0
    move.l D0, (A3)         ; 输出错误码
    halt
done:
    halt

; 如果超出了缓冲区的内存要求
; 必须改为流式处理，去掉输出缓冲区。因为内存无法装下最大的压缩结果。
; 程序必须边读、边压缩、边输出。同时，D3（输出长度计数器）仅作记录用，
; 不再作为溢出判断的条件。这要求 IO 输出端口具备接收无限长数据流的能力。
; Необходимо переключиться на потоковую обработку, удалив выходной буфер,
; поскольку память не может вместить максимальный сжатый результат. 
; Программа должна одновременно считывать, сжимать и выводить данные. 
; Кроме того, D3 (счетчик длины выходных данных) используется только 
; для записи и больше не используется в качестве условия для обнаружения переполнения. 
; Это требует, чтобы порт ввода-вывода был способен принимать бесконечно длинный поток данных.

; 需要输出缓冲区：
; 先计算，后提交 / Сначала вычисление, затем фиксация
; 在程序的规范中，明确要求：如果压缩后的结果超过 64 字节，需要向端口输出错误码 0xCCCCCCCC。
; 如果没有输出缓冲区，而是直接将压缩结果写入 IO 端口（流式输出），就会遇到一个致命问题：
; 当你发现输出结果超过了 64 字节时，前 64 个字节已经发送到了硬件端口。
; 你无法将已经发出去的数据“撤回”，然后再替换成错误码。
; Сначала выполните вычисления, затем отправьте. 
; Спецификация программы явно требует, чтобы, если сжатый результат превышает 64 байта,
; на порт выводился код ошибки 0xCCCCCCCC.

; Если буфер вывода отсутствует, и сжатый результат записывается непосредственно на порт ввода-вывода
; (потоковый вывод), возникает фатальная проблема: когда вы обнаружите,
; что результат вывода превышает 64 байта, первые 64 байта уже были отправлены на аппаратный порт.
; Вы не сможете «восстановить» уже отправленные данные и заменить их кодом ошибки.