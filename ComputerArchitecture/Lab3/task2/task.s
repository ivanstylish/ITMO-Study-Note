\ ───────────────────────────────────────────
\ 数据段：在 0x00 处预填 32 个 '_'（ASCII 95）
\ ───────────────────────────────────────────
.data
.org 0x00
buf0: .byte '_','_','_','_','_','_','_','_'       \ 缓冲区0x20对应32字节
buf1: .byte '_','_','_','_','_','_','_','_'
buf2: .byte '_','_','_','_','_','_','_','_'
buf3: .byte '_','_','_','_','_','_','_','_'

\ ───────────────────────────────────────────
\ 代码段：放在 0x100 避开缓冲区
\ ───────────────────────────────────────────
.text
.org 0x100
_start:
    lit 0
    a!              \ A = 0，指向缓冲区起始地址

\ ══════════════════════════════════════
\ 阶段一：读取、转换并存入内存
\ ══════════════════════════════════════
read_loop:
    @p 0x80         \ 读一个字符压栈 -> [char]
    dup             \ -> [char, char]
    lit 10          \ -> [char, char, 10]
    xor             \ -> [char, char ^ 10]
    if end_str      \ 若结果为0（遇到 \n），跳转到正常结束

    \ ----- 溢出检查 -----
    \ 若还没遇到 \n，且 A 已经等于 31，说明放不下 \0 了
    a               \ -> [char, A]
    lit 31          \ -> [char, A, 31]
    xor             \ -> [char, A ^ 31]
    if overflow     \ 若 A == 31，跳转到溢出处理

    \ ----- 大小写转换 -----
    \ ① 检查是否 < 'a' (97)
    dup
    inv
    lit 97
    +
    -if not_lower

    \ ② 检查是否 > 'z' (122)
    dup
    lit 122
    inv
    +
    -if not_lower

    \ ③ 转大写 (减 32)
    lit 31
    inv
    +

not_lower:
    \ 存入内存（字节写入低8位）
    @               \ [char, mem[A]]
    lit 255
    inv
    and             \ [char, mem[A] & 0xFFFFFF00]
    +               \ [new_word]
    !+              \ 存入 mem[A]，A++

    read_loop ;     \ 尾跳转回主循环

\ ══════════════════════════════════════
\ 异常分支：溢出处理
\ ══════════════════════════════════════
overflow:
    drop            \ 丢弃刚刚读到的溢出字符
    lit -858993460  \ 错误码 0xCCCCCCCC
    !p 0x84         \ 输出错误码到 0x84
    halt

\ ══════════════════════════════════════
\ 正常分支：写入 \0 并统一输出字符串
\ ══════════════════════════════════════
end_str:
    drop            \ 弹出栈顶多余的 '\n'

    \ 1. 写入 \0（最低字节清零即可）
    @               \ [mem[A]]
    lit 255
    inv
    and             \ 最低位清零 -> \0 写入低8位
    !               \ 存入 mem[A]（不自增 A）

    \ 2. 保存当前 A（字符串总长度）到 0x40
    a               \ [length]
    !p 0x40         \ 存到空闲内存 0x40
    lit 0
    a!              \ 重置 A = 0，准备从头输出

\ ══════════════════════════════════════
\ 输出循环：逐字节输出到 0x84
\ 【修复】遇到 \0（null byte）立即停止输出
\   原因：输入中可能含有内嵌 \0，
\         C字符串输出规范要求在首个 \0 处截止
\ ══════════════════════════════════════
output_loop:
    \ 判断 A 是否已到达字符串末尾（A == length）
    a               \ [A]
    @p 0x40         \ [A, length]
    xor             \ [A ^ length]
    if done         \ A == length -> 全部输出完毕

    \ 读取当前字节
    @+              \ [word_at_A]，A 自动 +1
    lit 255
    and             \ [char_byte] 提取低8位

    \ 【关键修复】检查是否为 \0
    dup             \ [char_byte, char_byte]
    if stop_output  \ 若 char_byte == 0，停止输出

    \ 不是 \0，正常输出到 0x84
    !p 0x84         \ 输出 char_byte 到端口 0x84

    output_loop ;   \ 循环下一个字符

\ 遇到 \0：丢弃字节，直接跳到结束
stop_output:
    drop            \ 弹出 char_byte（值为0，不输出）

done:
    halt