_start:
    lit 0
    a!              \ A = 0，指向缓冲区起始地址
\ 将 0 压入数据栈，然后用 a! 弹出并赋值给寄存器 A。A 将作为缓冲区的写指针。
\ Поместите 0 в стек данных, затем извлеките его с помощью a! и 
\ присвойте значение регистру A. Регистр A будет служить указателем записи в буфер.
