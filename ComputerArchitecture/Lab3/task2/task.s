\ 实验本质：从端口读取一串字符，将其中的小写字母转换为大写字母，存入指定的内存缓冲区（以C字符串格式，即以\0结尾），最后将结果输出，并具备完善的缓冲区溢出保护和异常处理机制。
\ 没有直接的比较指令：F32a 只能通过算术运算的结果来控制流。代码通过 xor 判断相等，通过 inv + + 配合 -if 来判断大小关系。
\ В коде отсутствуют инструкции прямого сравнения: F32a может управлять потоком только через результаты арифметических операций. 
\ Для определения равенства используется `xor`, а для определения соотношения размеров — `inv++` в сочетании с `-if`.
\ 32位架构下的字节寻址：F32a 是 32 位架构，但寄存器 A 在执行 !+ 或 @+ 时是按字节（+1）递增的。然而，读写操作 ! 和 @ 是按 32位字进行的**。
\ 因此，直接写入一个字符会覆盖该地址所在的整个 32 位字。代码使用了 & 0xFFFFFF00 的位掩码技巧来完美解决“只修改低8位，保留其他3个字节”的问题。
\ Байтовая адресация в 32-битной архитектуре: F32a — это 32-битная архитектура, но регистр A увеличивается на байт (+1) при выполнении `!+` или `@+`. 
\ Однако операции чтения и записи `!` и `@` выполняются над 32-битными словами. Поэтому прямая запись символа перезапишет все 32-битное слово по этому адресу. 
\ В коде используется техника битовой маски `&0xFFFFFF00` для идеального решения проблемы «изменения только младших 8 бит и сохранения остальных 3 байтов».

