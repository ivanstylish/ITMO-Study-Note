read_loop:
    @p 0x80         \ 读一个字符压栈 Прочитайте символ и поместите его в стек.-> [char]
    dup             \ -> [char, char]
    lit 10          \ -> [char, char, 10] 换行符\n的值
    xor             \ -> [char, char ^ 10]
    if end_str      \ 若结果为0（遇到 \n символ новой строки），跳转到正常结束
\@p 0x80：从内存映射 I/O 端口 0x80 读取一个字符（假设外部环境不断提供字符）。
\ dup：复制栈顶字符，备用。
\ dup: Скопировать верхний символ стека для последующего использования.
\ xor 与 if：根据手册，if 在栈顶为 0 时跳转。char ^ 10 如果为 0，说明读到的字符 ASCII 码是 10（即 \n），代表输入结束，跳转到正常结束逻辑。
\ Побитовое исключающее ИЛИ. Извлекает два верхних значения (символ и 10), выполняет XOR и помещает результат в стек. Если символ - \n(10), результат будет 0. 
\ Условный переход. Если верхнее значение стека равно 0 (встречен \n), переход к метке end_str. Значение извлекается из стека.

    \ ----- 溢出检查 ----- Проверка на переполнение
    \ 若还没遇到 \n，且 A 已经等于 31，说明放不下 \0 了
    \ Если мы еще не встречали \n, и A уже равно 31, это означает, что мы не можем отказаться от \0.
    \ 缓冲区总大小为 32 字节（索引 0~31）。由于 C 字符串必须在末尾留一个字节给 \0，所以实际最多只能存 31 个有效字符。如果此时 A 已经是 31，说明再存就会放不下 \0，触发溢出错误
    \ Общий размер буфера составляет 32 байта (индексы 0–31). Поскольку в строках C в конце должен оставаться байт для символа \0, он может хранить максимум 31 допустимый символ. 
    \ Если A уже равно 31, это означает, что хранение большего количества символов не позволит вместить \0, что вызовет ошибку переполнения.

    a               \ -> [char, A] Чтение регистра A. Помещает текущее значение регистра A (указатель буфера) в стек данных.
    lit 31          \ -> [char, A, 31] Помещает 31 в стек. Так как 32-байтовый буфер требует место для \0, максимальный допустимый индекс - 31.
    xor             \ -> [char, A ^ 31] Проверяет, равен ли A 31. Если равен, результат 0.
    if overflow     \ 若 A == 31，跳转到溢出处理 Если верх стека равен 0 (A == 31), переход к обработке переполнения.

    \ ----- 大小写转换 -----Преобразование регистра
    \ ① 检查是否 < 'a' (97) 下界
    dup             \ Дублирует верхний символ стека.
    inv             \ (按位取反，等价于 -char - 1) Побитовое инвертирование. Эквивалентно дополнению до двух минус 1 (~char). Используется для последующего вычитания.
    lit 97          \ Помещает ASCII-код 'a' (97) в стек.
    +               \ 即 [char, 96 - char]
                    \ Сложение. Вычисляет ~char + 97, математически эквивалентно 96 - char. Устанавливает флаг переноса.
    -if not_lower   \ Условный переход при неотрицательном значении. 
                    \ Если верхнее значение >= 0 (т.е. char <= 96, символ меньше 'a'), переход к not_lower.

    \ 因为小写字母 'a' 的 ASCII 是 97，所以 char <= 96 就代表字符小于 'a'，不是小写字母，直接跳过转换。
    \ Поскольку ASCII-значение строчной буквы «a» равно 97, условие char <= 96 означает, 
    \ что символ меньше «a» и не является строчной буквой, поэтому преобразование пропускается.


    \ ② 检查是否 > 'z' (122) 上界 
    dup             \ Снова дублирует символ.
    lit 122         \ Помещает ASCII-код 'z' (122) в стек.
    inv             \ (即 -123) Побитовое инвертирование, дает ~122
    +               \ Сложение. Вычисляет char - 123.
    -if not_lower
    \ 如果 char - 123 >= 0，意味着 char >= 123。因为 'z' 是 122，所以这就代表字符大于 'z'，不是小写字母，跳过转换。
    \ Если char - 123 >= 0, это означает, что char >= 123. Поскольку 'z' равно 122, это означает, 
    \ что символ больше 'z', а не строчная буква, поэтому преобразование пропускается.

    \ ③ 转大写 (减 32) Выполнение вычитания 32 для верхнего регистра
    lit 31          \ Помещает 31 в стек.
    inv             \ (即 -32) Инвертирование дает -32.
    +               \ Прибавляет -32 к верхнему символу, завершая преобразование строчной буквы в заглавную.
\ После выполнения двух описанных выше шагов символ в верхней части стека должен находиться между 'a' и 'z'. 
\ Для преобразования строчных букв в прописные достаточно вычесть 32. В коде вычитание выполняется с помощью ~31 + char.

not_lower:
    \ 存入内存（字节写入低8位） Безопасная побайтовая запись в 32-битную память
    @               \ [char, mem[A]] (Прочитайте все 32-битное слово, на которое указывает указатель A и помещает в стек.)
    lit 255         \ Помещает 255 (двоичное 0x000000FF) в стек.
    inv             \ (~255 即 0xFFFFFF00) Инвертирование, получает маску 0xFFFFFF00.
    and             \ [char, mem[A] & 0xFFFFFF00] Побитовое И. Сохраняет старшие 24 бита исходного слова памяти, 
                    \ обнуляет младшие 8 бит, освобождая место для записи.
    +               \ Складывает обнуленное слово памяти с записываемым символом, формируя новое 32-битное слово.
    !+              \ 存入 mem[A](Запись обратно в память по адресу A), затем автоматически увеличивает A на 1

    read_loop ;     \ 尾跳转回主循环 здесь выступает как безусловный переход обратно к read_loop для чтения следующего символа.
\ Сначала считываем исходное значение, используем 0xFFFFFF00 в качестве маски И, 
\ обнуляем младшие 8 бит целевого байта (например, до 0x5F5F5F00), 
\ затем добавляем новый символ ASCII (до 0x5F5F5F41) и, наконец, записываем его обратно. Оператор `!+` 
\ идеально реализует запись и автоматически перемещает указатель на следующий байт.


