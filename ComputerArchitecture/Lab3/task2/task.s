overflow:
    drop            \ 丢弃刚刚读到的溢出字符 бросить только что прочитанный символ переполнения.
    lit -858993460  \ 错误码 0xCCCCCCCC Записать код ошибки в стек данных.
    !p 0x84         \ 输出错误码到 0x84 выводить код ошибки в адрес 0x84
    halt

\ ══════════════════════════════════════
\ 正常分支：写入 \0 并统一输出字符串 Нормальная ветка: запись \0
\ ══════════════════════════════════════
end_str:
    drop            \ 弹出栈顶多余的 '\n' Удалить лишний символ '\n' из верхней части стека.

    \ 1. 写入 \0（最低字节清零即可）
    @               \ [mem[A]]
    lit 255
    inv
    and             \ 最低8位清零 -> \0 写入低8位
    !               \ 存入 mem[A]（不自增 A） Это эквивалентно написанию символа '\0'.

    \ 2. 保存当前 A（字符串总长度）到 0x40 Сохранение длины и сброс указателя
    a               \ [length] В этот момент значение A как раз равно длине строки, оно помещается в стек.
    !p 0x40         \ 存到空闲内存 0x40 Длина временно хранится по адресу памяти 0x40.
    lit 0
    a!              \ 重置 A = 0，准备从头输出 Обновите A до значения 0, подготовьтесь к выводу с самого начала.

\ ══════════════════════════════════════
\ 输出循环：逐字节输出到 0x84
\ 遇到 \0（null byte）立即停止输出
\   原因：输入中可能含有内嵌 \0，
\   C字符串输出规范要求在首个 \0 处截止 
\ ══════════════════════════════════════
output_loop: Цикл вывода: проверка границ
    \ 判断 A 是否已到达字符串末尾（A == length）
    a               \ стек [A] Помещает текущий указатель A в стек.
    @p 0x40         \ Считывает сохраненную ранее длину строки из 0x40.
    xor             \ Сравнение посредством XOR. Проверяет, равен ли A длине.
    if done         \ A == length -> 全部输出完毕 Все выходные данные получены переход к завершению.

    \ 读取当前字节 Чтение и извлечение байта
    @+              \ Чтение с инкрементом. Считывает 32-битное слово по адресу A, помещает в стек, затем A увеличивается на 1.
    lit 255
    and             \ Побитовое И с 0xFF. Фильтрует мешающие старшие биты, оставляя только младшие 8 бит действительного символа.

    \ 检查是否为 \0 Проверка на встроенный  
    dup             \ [char_byte, char_byte]
    if stop_output  \ Если значение символа равно 0 (т.е. входная строка изначально содержала \0), 
                    \ немедленный переход к логике остановки вывода, предотвращая вывод мусора за пределами

    \ 不是 \0，正常输出到 0x84
    !p 0x84         \ Выводит символ на порт 0x84.

    output_loop ;   \ Безусловный переход для продолжения вывода следующего символа.

\ 遇到 \0：丢弃字节，直接跳到结束 Встреча с \0: Отбросить байт и перейти непосредственно в конец.
stop_output:
    drop            \ бросить прочитанный нулевой символ.

done:
    halt