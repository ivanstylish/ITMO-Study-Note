\ ══════════════════════════════════════
\ 阶段一：读取、转换并存入内存
\ ══════════════════════════════════════
read_loop:
    @p 0x80         \ 读一个字符压栈 Прочитайте символ и поместите его в стек.-> [char]
    dup             \ -> [char, char]
    lit 10          \ -> [char, char, 10]
    xor             \ -> [char, char ^ 10]
    if end_str      \ 若结果为0（遇到 \n символ новой строки），跳转到正常结束
\@p 0x80：从内存映射 I/O 端口 0x80 读取一个字符（假设外部环境不断提供字符）。
\dup：复制栈顶字符，备用。
\xor 与 if：根据手册，if 在栈顶为 0 时跳转。char ^ 10 如果为 0，说明读到的字符 ASCII 码是 10（即 \n），代表输入结束，跳转到正常结束逻辑。

    \ ----- 溢出检查 -----
    \ 若还没遇到 \n，且 A 已经等于 31，说明放不下 \0 了
    \缓冲区总大小为 32 字节（索引 0~31）。由于 C 字符串必须在末尾留一个字节给 \0，所以实际最多只能存 31 个有效字符。如果此时 A 已经是 31，说明再存就会放不下 \0，触发溢出错误
    a               \ -> [char, A]
    lit 31          \ -> [char, A, 31]
    xor             \ -> [char, A ^ 31]
    if overflow     \ 若 A == 31，跳转到溢出处理

    \ ----- 大小写转换 -----
    \ ① 检查是否 < 'a' (97)
    dup             \ 栈: [char, char]
    inv             \ 栈: [char, ~char]  (按位取反，等价于 -char - 1)
    lit 97          \ 栈: [char, ~char, 97]
    +               \ 栈: [char, ~char + 97] 即 [char, 96 - char]
    -if not_lower   \ 栈: [char]
    \ 因为小写字母 'a' 的 ASCII 是 97，所以 char <= 96 就代表字符小于 'a'，不是小写字母，直接跳过转换。
    \ Поскольку ASCII-значение строчной буквы «a» равно 97, условие char <= 96 означает, 
    \ что символ меньше «a» и не является строчной буквой, поэтому преобразование пропускается.


    \ ② 检查是否 > 'z' (122)
    dup
    lit 122
    inv             \ 栈: [char, char, ~122] (即 -123)
    +               \ 栈: [char, char - 123]
    -if not_lower
    \ 如果 char - 123 >= 0，意味着 char >= 123。因为 'z' 是 122，所以这就代表字符大于 'z'，不是小写字母，跳过转换。
    \ Если char - 123 >= 0, это означает, что char >= 123. Поскольку 'z' равно 122, это означает, 
    \ что символ больше 'z', а не строчная буква, поэтому преобразование пропускается.

    \ ③ 转大写 (减 32)
    lit 31          \ 栈: [char, 31]
    inv             \ 栈: [char, ~31] (即 -32)
    +               \ 栈: [char - 32]
\ После выполнения двух описанных выше шагов символ в верхней части стека должен находиться между 'a' и 'z'. 
\ Для преобразования строчных букв в прописные достаточно вычесть 32. В коде вычитание выполняется с помощью ~31 + char.

not_lower:
    \ 存入内存（字节写入低8位）
    @               \ [char, mem[A]] (Прочитайте все 32-битное слово, на которое указывает указатель A.)
    lit 255         \ 栈: [char, mem[A], 255]
    inv             \ 栈: [char, mem[A], ~255]  (~255 即 0xFFFFFF00)
    and             \ [char, mem[A] & 0xFFFFFF00]
    +               \ [new_word]
    !+              \ 存入 mem[A](Запись обратно в память по адресу A)，A++

    read_loop ;     \ 尾跳转回主循环
\ Сначала считываем исходное значение, используем 0xFFFFFF00 в качестве маски И, 
\ обнуляем младшие 8 бит целевого байта (например, до 0x5F5F5F00), 
\ затем добавляем новый символ ASCII (до 0x5F5F5F41) и, наконец, записываем его обратно. Оператор `!+` 
\ идеально реализует запись и автоматически перемещает указатель на следующий байт.


\ ══════════════════════════════════════
\ 异常分支：溢出处理
\ ══════════════════════════════════════
overflow:
    drop            \ 丢弃刚刚读到的溢出字符 бросить только что прочитанный символ переполнения.
    lit -858993460  \ 错误码 0xCCCCCCCC Записать код ошибки в стек данных.
    !p 0x84         \ 输出错误码到 0x84 выводить код ошибки в адрес 0x84
    halt

\ ══════════════════════════════════════
\ 正常分支：写入 \0 并统一输出字符串
\ ══════════════════════════════════════
end_str:
    drop            \ 弹出栈顶多余的 '\n' Удалить лишний символ '\n' из верхней части стека.

    \ 1. 写入 \0（最低字节清零即可）
    @               \ [mem[A]]
    lit 255
    inv
    and             \ 最低8位清零 -> \0 写入低8位
    !               \ 存入 mem[A]（不自增 A） Это эквивалентно написанию символа '\0'.

    \ 2. 保存当前 A（字符串总长度）到 0x40
    a               \ [length]
    !p 0x40         \ 存到空闲内存 0x40 Длина временно хранится по адресу памяти 0x40.
    lit 0
    a!              \ 重置 A = 0，准备从头输出 Обновите A до значения 0, подготовьтесь к выводу с самого начала.

\ ══════════════════════════════════════
\ 输出循环：逐字节输出到 0x84
\ 遇到 \0（null byte）立即停止输出
\   原因：输入中可能含有内嵌 \0，
\   C字符串输出规范要求在首个 \0 处截止
\ ══════════════════════════════════════
output_loop:
    \ 判断 A 是否已到达字符串末尾（A == length）
    a               \ стек [A]
    @p 0x40         \ [A, length] 
    xor             \ [A ^ length]
    if done         \ A == length -> 全部输出完毕 Все выходные данные получены

    \ 读取当前字节
    @+              \ [word_at_A]，A 自动 +1 
    lit 255
    and             \ [char_byte] 提取低8位 Извлеките младшие 8 бит.

    \ 【关键修复】检查是否为 \0
    dup             \ [char_byte, char_byte]
    if stop_output  \ 若 char_byte == 0，停止输出

    \ 不是 \0，正常输出到 0x84
    !p 0x84         \ 输出 char_byte 到端口 0x84 Вывод символов на порт 0x84

    output_loop ;   \ 循环下一个字符 Перейти к следующему символу

\ 遇到 \0：丢弃字节，直接跳到结束 Встреча с \0: Отбросить байт и перейти непосредственно в конец.
stop_output:
    drop            \ 弹出 char_byte（值为0，不输出）бросить прочитанный нулевой символ.

done:
    halt