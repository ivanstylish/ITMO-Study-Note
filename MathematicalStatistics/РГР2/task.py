print("1. Гипотеза о равенстве средних X1 и X2 (параметрический t-критерий).")
print("2. Гипотеза о параметре нормального распределения для X3 (t-критерий или χ2).")
print("3. Гипотеза о совпадении распределений X1 и X2 (непараметрический критерий Манна-Уитни). Выводы параметрического и непараметрического критериев совпали, что говорит об устойчивости результата.")
print("4. Гипотеза о согласии выборки X4 с заданным законом распределения (критерий Пирсона).")
print("Во всех задачах был выбран уровень значимости α=0.05. Решения принимались на основе сравнения p-value с α. Результаты позволяют сделать обоснованные выводы о свойствах генеральных совокупностей на основе выборочных данных.")