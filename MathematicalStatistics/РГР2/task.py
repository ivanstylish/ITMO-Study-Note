import pandas as pd
import numpy as np
from scipy import stats

# ==========================================
# 0. Чтение данных из CSV файла
# ==========================================
# Укажите путь к вашему CSV-файлу. Разделитель - точка с запятой ;
file_path = 'RGR2_A-4_X1-X4.csv'
df = pd.read_csv(file_path, sep=';')

X1 = df['X1'].values
X2 = df['X2'].values
X3 = df['X3'].values
X4 = df['X4'].values

n = len(df)
alpha = 0.05  # Уровень значимости по умолчанию

print(f"Объем выборки n = {n}, Уровень значимости α = {alpha}\n")

# ==========================================
# Этап 4.2: Проверка гипотезы о равенстве математических ожиданий (X1, X2)
# ==========================================
print("="*60)
print("Этап 4.2: Проверка гипотезы о равенстве средних (параметрический)")
print("="*60)
# H0: μ1 = μ2 (средние равны)
# H1: μ1 ≠ μ2 (средние не равны, двусторонняя альтернатива)
# Критерий: Двухвыборочный t-критерий Стьюдента (предполагается равенство дисперсий)

m, n2 = len(X1), len(X2)
X1_bar, X2_bar = np.mean(X1), np.mean(X2)

# Используем встроенную функцию scipy
t_stat, p_val_ttest = stats.ttest_ind(X1, X2, equal_var=True)

print(f"H0: μ1 = μ2, H1: μ1 ≠ μ2")
print(f"Выборочное среднее X1: {X1_bar:.2f}, X2: {X2_bar:.2f}")
print(f"Наблюдаемое значение статистики h_набл = {t_stat:.4f}")
print(f"p-value = {p_val_ttest:.4f}")
if p_val_ttest < alpha:
    print(f"Вывод: p-value ({p_val_ttest:.4f}) < α ({alpha}), отвергаем H0. Есть основания считать средние различными.")
else:
    print(f"Вывод: p-value ({p_val_ttest:.4f}) >= α ({alpha}), нет оснований отвергнуть H0. Средние статистически не различаются.")


# ==========================================
# Этап 4.3: Проверка гипотезы о параметре нормального распределения (X3)
# ==========================================
print("\n" + "="*60)
print("Этап 4.3: Проверка гипотезы о параметре нормального распределения (X3)")
print("="*60)
# ВАЖНО: Подставьте значения из вашей карточки варианта!
# Здесь по умолчанию проверяется H0: μ = 95.0
mu_0 = 95.0
print(f"【По карточке варианта проверяется H0: μ = {mu_0}】")

# Критерий: Одновыборочный t-критерий Стьюдента
t_stat_1samp, p_val_1samp = stats.ttest_1samp(X3, popmean=mu_0)

print(f"H0: μ = {mu_0}, H1: μ ≠ {mu_0}")
print(f"Выборочное среднее X3: {np.mean(X3):.2f}")
print(f"Наблюдаемое значение статистики h_набл = {t_stat_1samp:.4f}")
print(f"p-value = {p_val_1samp:.4f}")
if p_val_1samp < alpha:
    print(f"Вывод: p-value ({p_val_1samp:.4f}) < α ({alpha}), отвергаем H0. Среднее значимо отличается от {mu_0}.")
else:
    print(f"Вывод: p-value ({p_val_1samp:.4f}) >= α ({alpha}), нет оснований отвергнуть H0. Можно считать среднее равным {mu_0}.")

# (Если в карточке указана гипотеза о дисперсии H0: σ^2 = σ0^2, раскомментируйте код ниже и удалите t-тест)
