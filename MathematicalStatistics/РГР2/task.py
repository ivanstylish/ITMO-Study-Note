# chi2_stat_var = (len(X3) - 1) * np.var(X3, ddof=1) / sigma_0_sq
# p_val_var = 2 * min(stats.chi2.cdf(chi2_stat_var, df=len(X3)-1), 1 - stats.chi2.cdf(chi2_stat_var, df=len(X3)-1))
# print(f"H0: σ^2 = {sigma_0_sq}... p-value = {p_val_var:.4f}")


# ==========================================
# Этап 4.4: Непараметрический критерий для двух выборок (X1, X2)
# ==========================================
print("\n" + "="*60)
print("Этап 4.4: Непараметрический критерий Манна-Уитни (X1, X2)")
print("="*60)
# H0: Распределения совпадают
# H1: Распределения различаются
u_stat, p_val_mannwhitneyu = stats.mannwhitneyu(X1, X2, alternative='two-sided')

print(f"H0: Распределения X1 и X2 одинаковы, H1: Распределения различаются")
print(f"Наблюдаемое значение статистики U = {u_stat:.4f}")
print(f"p-value = {p_val_mannwhitneyu:.6f}")
if p_val_mannwhitneyu < alpha:
    print(f"Вывод: p-value ({p_val_mannwhitneyu:.6f}) < α ({alpha}), отвергаем H0. Распределения значимо различаются.")
else:
    print(f"Вывод: p-value ({p_val_mannwhitneyu:.6f}) >= α ({alpha}), нет оснований отвергнуть H0. Различий в распределениях не обнаружено.")

print("\n--> Сравнение выводов этапов 4.2 и 4.4:")
print("Параметрический критерий (Стьюдент) предполагает нормальность распределения, а непараметрический (Манна-Уитни) — нет.")
print("Если выводы совпали, это подтверждает robustness (устойчивость) вывода. Если бы они различались, это могло бы означать наличие сильных выбросов или нарушение нормальности, при котором t-критерий давал бы ложный результат, и доверять следовало бы критерию Манна-Уитни.")


# ==========================================
# Этап 4.5: Критерий согласия Пирсона (X4)
# ==========================================
print("\n" + "="*60)
print("Этап 4.5: Критерий согласия Пирсона (χ2) для X4")
print("="*60)
# ВАЖНО: Подставьте закон распределения и параметры из вашей карточки варианта!
# Здесь по умолчанию: Экспоненциальное распределение Exp(λ=0.1), среднее = 10
lambda_0 = 0.1
print(f"【По карточке варианта проверяется согласие с Exp(λ={lambda_0})】")

# 1. Разбиение на интервалы и подсчет наблюдаемых частот
num_bins = 7
obs_freq, bin_edges = np.histogram(X4, bins=num_bins)

# ИСПРАВЛЕНИЕ ОШИБКИ: Для экспоненциального распределения левая граница должна быть 0, правая - +inf
# Только так сумма теоретических вероятностей будет равна 1, а сумма ожидаемых частот - объему выборки n
bin_edges[0] = 0.0
bin_edges[-1] = np.inf

# 2. Вычисление теоретических вероятностей и ожидаемых частот
expected_freq = []
for i in range(len(bin_edges) - 1):
    a, b = bin_edges[i], bin_edges[i+1]
    # F(b) - F(a) для экспоненциального распределения
    prob = stats.expon.cdf(b, scale=1/lambda_0) - stats.expon.cdf(a, scale=1/lambda_0)
    expected_freq.append(prob * n)

expected_freq = np.array(expected_freq)

# 3. Объединение соседних интервалов, если ожидаемая частота < 5
# (Необходимое условие корректного применения критерия Пирсона)
while any(expected_freq < 5) and len(expected_freq) > 2:
    for i in range(len(expected_freq)):
        if expected_freq[i] < 5:
            # Объединяем со следующим интервалом, если он есть, иначе с предыдущим
            if i < len(expected_freq) - 1:
                obs_freq[i] += obs_freq[i+1]
                expected_freq[i] += expected_freq[i+1]
                obs_freq = np.delete(obs_freq, i+1)
                expected_freq = np.delete(expected_freq, i+1)
                bin_edges = np.delete(bin_edges, i+1)
            else:
                obs_freq[i-1] += obs_freq[i]
                expected_freq[i-1] += expected_freq[i]
                obs_freq = np.delete(obs_freq, i)
                expected_freq = np.delete(expected_freq, i)
                bin_edges = np.delete(bin_edges, i)
            break # Перезапуск цикла, так как индексы изменились

# 4. Вычисление статистики χ2
# s_param - число оцененных по выборке параметров (если параметры заданы в карточке, s=0)
s_param = 0
k_intervals = len(obs_freq)
df_chi2 = k_intervals - s_param - 1

chi2_stat, p_val_chi2 = stats.chisquare(f_obs=obs_freq, f_exp=expected_freq, ddof=s_param)

# Форматируем вывод границ (inf заменяем на +∞ для красоты)
edges_str = [str(round(x, 2)) if x != np.inf else '+∞' for x in bin_edges]

print(f"H0: X4 подчиняется Exp({lambda_0}), H1: Не подчиняется")
print(f"Границы интервалов: {edges_str}")
print(f"Наблюдаемые частоты (n_k):  {obs_freq}")
print(f"Ожидаемые частоты (np_k):   {np.round(expected_freq, 2)}")
print(f"Число степеней свободы (k-s-1) = {df_chi2}")
print(f"Наблюдаемое значение статистики χ2 = {chi2_stat:.4f}")
print(f"p-value = {p_val_chi2:.4f}")

if p_val_chi2 < alpha:
    print(f"Вывод: p-value ({p_val_chi2:.4f}) < α ({alpha}), отвергаем H0. Данные не согласуются с теоретическим распределением.")
else:
    print(f"Вывод: p-value ({p_val_chi2:.4f}) >= α ({alpha}), нет оснований отвергнуть H0. Данные хорошо согласуются с теоретическим распределением.")


